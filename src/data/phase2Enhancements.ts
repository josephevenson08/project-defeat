function statPriorityFor(playerClass: string, spec: string, role: EnhancementRole) {
  const exact: Record<string, string> = {
    'Shaman:Enhancement': 'Hit to cap > Expertise > Strength / Attack Power > Agility > Crit',
    'Hunter:Survival': 'Hit to cap > Agility > Attack Power > Crit',
    'Mage:Arcane': 'Spell Hit to cap > Spell Damage > Intellect > Haste > Crit',
    'Mage:Fire': 'Spell Hit to cap > Spell Damage > Crit > Haste',
    'Warlock:Destruction': 'Spell Hit to cap > Spell Damage > Crit > Haste',
    'Druid:Feral Bear': 'Survival breakpoint > Armor > Stamina > Agility > Threat',
    'Paladin:Protection': 'Uncrushability / Defense > Stamina > Spell Damage > Avoidance',
    'Priest:Shadow': 'Spell Hit to cap > Shadow Damage > Haste > Crit',
  }
  return exact[`${playerClass}:${spec}`] ?? {
    melee: 'Hit / Expertise caps > primary stat > Attack Power > Crit / Haste',
    hunter: 'Hit to cap > Agility > Attack Power > Crit',
    caster: 'Spell Hit to cap > Spell Damage > Haste / Crit > Intellect',
    healer: 'Healing Power > mana sustain > Haste / Crit > Intellect',
    tank: 'Defense and encounter survival > Stamina / Armor > avoidance > threat',
  }[role]
}

export function getPhase2EnhancementPlan(playerClass: string, spec: string, role: EnhancementRole): EnhancementPlan {
  const base = phase2Enhancements.filter((entry) => entry.roles.includes(role))
  let enchants = base.filter((entry) => entry.kind === 'Enchant')
  let gems = base.filter((entry) => entry.kind === 'Gem')
  const notes = [
    'Tier S is the default Phase 2 recommendation; Tier A is situational, cap-dependent, or a close faction alternative.',
    'Socket bonuses are only worth taking when the bonus plus activation requirement beats the lost primary-stat gems.',
    'Profession selections highlight access and personal bonuses; they do not hide valid recommendations.',
  ]

  if (role === 'caster') {
    const sunfireSpecs = ['Arcane', 'Fire', 'Balance']
    const soulfrostSpecs = ['Frost', 'Shadow', 'Affliction', 'Demonology', 'Destruction']
    enchants = enchants.map((entry) => {
      if (entry.id === 'sunfire') return { ...entry, tier: sunfireSpecs.includes(spec) ? 'S' : 'B' }
      if (entry.id === 'soulfrost') return { ...entry, tier: soulfrostSpecs.includes(spec) ? 'S' : 'B' }
      return entry
    })
    if (spec === 'Arcane') {
      gems = gems.map((entry) => entry.id === 'insightful' ? { ...entry, tier: 'S' } : entry.id === 'chaotic' ? { ...entry, tier: 'A' } : entry)
      notes.push('Arcane can rank Insightful above Chaotic when mana-limited; fight length and group support decide the winner.')
    }
  }

  if (spec === 'Feral Cat' || spec === 'Feral Bear') {
    enchants = enchants.map((entry) => {
      if (entry.id === 'major-agility-weapon') return { ...entry, tier: 'S' }
      if (entry.id === 'mongoose') return { ...entry, tier: 'B' }
      return entry
    })
    notes.push('Mongoose does not proc from attacks while shapeshifted, so Feral uses the static +35 Agility weapon enchant.')
  }

  if (playerClass === 'Paladin' && spec === 'Protection') {
    notes.push('Protection Paladin glove and weapon choices split between threat and survival sets; no single enchant wins every encounter.')
  }

  if (!specsByRole[role].includes(spec) && !(spec === 'Holy' || spec === 'Protection' || spec === 'Restoration')) {
    notes.push('This profile is using the role baseline and needs a dedicated guide audit.')
  }

  return {
    statPriority: statPriorityFor(playerClass, spec, role),
    enchants: enchants.sort((a, b) => a.slot.localeCompare(b.slot) || tierOrder[a.tier] - tierOrder[b.tier]),
    gems: gems.sort((a, b) => a.slot.localeCompare(b.slot) || tierOrder[a.tier] - tierOrder[b.tier]),
    notes,
  }
}
