export type EnhancementRole = 'melee' | 'caster' | 'healer' | 'tank' | 'hunter'
export type EnhancementTier = 'S' | 'A' | 'B'
export type EnhancementKind = 'Enchant' | 'Gem'

export type EnhancementMaterial = {
  name: string
  quantity?: number
}

export type MaterialSource = {
  name: string
  source: string
  sourceUrl?: string
}

export type Enhancement = {
  id: string
  name: string
  kind: EnhancementKind
  slot: string
  tier: EnhancementTier
  effect: string
  roles: EnhancementRole[]
  materials: EnhancementMaterial[]
  learnedFrom: string
  sourceType: 'Trainer' | 'Reputation' | 'Vendor' | 'World Drop' | 'Boss Drop' | 'Profession'
  sourceUrl: string
  professionRequired?: 'Enchanting' | 'Jewelcrafting'
  activation?: string
  note?: string
}

export type EnhancementPlan = {
  statPriority: string
  enchants: Enhancement[]
  gems: Enhancement[]
  notes: string[]
}

